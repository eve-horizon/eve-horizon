#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"
CONFIG_FILE="$PROJECT_ROOT/.eve-horizon.yaml"

# Source common utilities
source "$SCRIPT_DIR/_common.sh"

# Source current config (for defaults)
source "$SCRIPT_DIR/_config.sh"

show_help() {
  echo "Eve Horizon Configuration"
  echo ""
  echo "Usage: eh configure [options]"
  echo ""
  echo "Options:"
  echo "  --show                 Show current configuration"
  echo "  --instance <name>      Set instance name (docker compose prefix)"
  echo "  --base-port <port>     Set base port number"
  echo "  --k8s-owner            Mark this instance as the k8s cluster owner"
  echo "  --no-k8s-owner         Mark this instance as NOT the k8s cluster owner"
  echo "  --staging-owner        Mark this repo as the staging environment owner"
  echo "  --no-staging-owner     Mark this repo as NOT the staging environment owner"
  echo "  --staging-owner-repo <path>  Set the repo path that owns staging"
  echo "  --staging-kubeconfig <path>  Set the staging kubeconfig path"
  echo "  --non-interactive      Skip prompts, use provided values or defaults"
  echo "  --help                 Show this help"
  echo ""
  echo "Port Allocation (default base: 4800):"
  echo "  API:          base + 1  (default: 4801)"
  echo "  Orchestrator: base + 2  (default: 4802)"
  echo "  Database:     base + 3  (default: 4803)"
  echo "  Worker:       base + 11 (default: 4811)"
  echo ""
  echo "K8s Ownership:"
  echo "  Only one instance should be the k8s owner (k8s_owner: true)."
  echo "  Non-owners will be warned before running k8s commands."
  echo "  This prevents multiple agents from conflicting on the shared k3d cluster."
  echo ""
  echo "Staging Ownership:"
  echo "  Only one repo should be responsible for staging operations."
  echo "  Non-owners should avoid tagging release-v* or running staging kubectl."
  echo ""
  echo "Examples:"
  echo "  eh configure                    # Interactive setup"
  echo "  eh configure --show             # Show current config"
  echo "  eh configure --instance myapp --base-port 5000"
  echo "  eh configure --k8s-owner        # Mark as k8s owner"
}

write_config() {
  local instance="$1"
  local base_port="$2"
  local k8s_owner="$3"
  local staging_owner="$4"
  local staging_owner_repo="$5"
  local staging_kubeconfig="$6"

  cat > "$CONFIG_FILE" << EOF
# Eve Horizon instance configuration
# Generated by: ./bin/eh configure

instance: $instance
base_port: $base_port
k8s_owner: $k8s_owner
staging_owner: $staging_owner
staging_owner_repo: $staging_owner_repo
staging_kubeconfig: $staging_kubeconfig

# Computed ports:
#   API:          $((base_port + 1))
#   Orchestrator: $((base_port + 2))
#   Database:     $((base_port + 3))
#   Worker:       $((base_port + 11))

# K8s Ownership:
#   Only one instance should have k8s_owner: true
#   This designates which repo "owns" the shared k3d cluster
#   Non-owners will be warned before running k8s commands
#
# Staging Ownership:
#   Only one repo should have staging_owner: true
#   This designates which repo is responsible for staging operations
#   Non-owners should avoid tagging release-v* or running staging kubectl
EOF

  echo -e "${GREEN}Configuration saved to .eve-horizon.yaml${NC}"
}

# Parse arguments
SHOW_ONLY=false
NON_INTERACTIVE=false
ARG_INSTANCE=""
ARG_BASE_PORT=""
ARG_K8S_OWNER=""
ARG_STAGING_OWNER=""
ARG_STAGING_OWNER_REPO=""
ARG_STAGING_KUBECONFIG=""

while [[ $# -gt 0 ]]; do
  case "$1" in
    --show)
      SHOW_ONLY=true
      shift
      ;;
    --instance)
      ARG_INSTANCE="$2"
      shift 2
      ;;
    --base-port)
      ARG_BASE_PORT="$2"
      shift 2
      ;;
    --k8s-owner)
      ARG_K8S_OWNER="true"
      shift
      ;;
    --no-k8s-owner)
      ARG_K8S_OWNER="false"
      shift
      ;;
    --staging-owner)
      ARG_STAGING_OWNER="true"
      shift
      ;;
    --no-staging-owner)
      ARG_STAGING_OWNER="false"
      shift
      ;;
    --staging-owner-repo)
      ARG_STAGING_OWNER_REPO="$2"
      shift 2
      ;;
    --staging-kubeconfig)
      ARG_STAGING_KUBECONFIG="$2"
      shift 2
      ;;
    --non-interactive)
      NON_INTERACTIVE=true
      shift
      ;;
    --help|-h)
      show_help
      exit 0
      ;;
    *)
      echo "Unknown option: $1"
      show_help
      exit 1
      ;;
  esac
done

# Show-only mode
if [[ "$SHOW_ONLY" == "true" ]]; then
  eve_show_config
  exit 0
fi

# Determine values
NEW_INSTANCE="${ARG_INSTANCE:-$EVE_INSTANCE}"
NEW_BASE_PORT="${ARG_BASE_PORT:-$EVE_BASE_PORT}"
NEW_K8S_OWNER="${ARG_K8S_OWNER:-$EVE_K8S_OWNER}"
NEW_STAGING_OWNER="${ARG_STAGING_OWNER:-$EVE_STAGING_OWNER}"
NEW_STAGING_OWNER_REPO="${ARG_STAGING_OWNER_REPO:-$EVE_STAGING_OWNER_REPO}"
NEW_STAGING_KUBECONFIG="${ARG_STAGING_KUBECONFIG:-$EVE_STAGING_KUBECONFIG}"

# Interactive mode
if [[ "$NON_INTERACTIVE" == "false" && -z "$ARG_INSTANCE" && -z "$ARG_BASE_PORT" && -z "$ARG_K8S_OWNER" && -z "$ARG_STAGING_OWNER" && -z "$ARG_STAGING_OWNER_REPO" && -z "$ARG_STAGING_KUBECONFIG" ]]; then
  echo -e "${CYAN}Eve Horizon Configuration Wizard${NC}"
  echo ""

  # Instance name
  read -p "Instance name [$NEW_INSTANCE]: " input_instance
  NEW_INSTANCE="${input_instance:-$NEW_INSTANCE}"

  # Base port
  read -p "Base port [$NEW_BASE_PORT]: " input_port
  NEW_BASE_PORT="${input_port:-$NEW_BASE_PORT}"

  # K8s ownership
  echo ""
  echo "K8s Ownership: Only one instance should own the shared k3d cluster."
  echo "  - Owners can run 'eh k8s *' commands freely"
  echo "  - Non-owners will be warned/prompted before k8s operations"
  read -p "Is this instance the k8s owner? (y/n) [$NEW_K8S_OWNER]: " input_k8s
  if [[ "$input_k8s" =~ ^[Yy] ]]; then
    NEW_K8S_OWNER="true"
  elif [[ "$input_k8s" =~ ^[Nn] ]]; then
    NEW_K8S_OWNER="false"
  fi

  # Staging ownership
  echo ""
  echo "Staging Ownership: Only one repo should control staging operations."
  echo "  - Owners can tag release-v* and run staging kubectl"
  echo "  - Non-owners should avoid staging changes without approval"
  read -p "Is this repo the staging owner? (y/n) [$NEW_STAGING_OWNER]: " input_staging
  if [[ "$input_staging" =~ ^[Yy] ]]; then
    NEW_STAGING_OWNER="true"
  elif [[ "$input_staging" =~ ^[Nn] ]]; then
    NEW_STAGING_OWNER="false"
  fi

  if [[ "$NEW_STAGING_OWNER" == "true" ]]; then
    NEW_STAGING_OWNER_REPO="${NEW_STAGING_OWNER_REPO:-$PROJECT_ROOT}"
  elif [[ -n "$NEW_STAGING_OWNER_REPO" ]]; then
    read -p "Staging owner repo path (optional) [$NEW_STAGING_OWNER_REPO]: " input_staging_repo
    NEW_STAGING_OWNER_REPO="${input_staging_repo:-$NEW_STAGING_OWNER_REPO}"
  fi

  read -p "Staging kubeconfig path [$NEW_STAGING_KUBECONFIG]: " input_staging_kubeconfig
  NEW_STAGING_KUBECONFIG="${input_staging_kubeconfig:-$NEW_STAGING_KUBECONFIG}"

  echo ""
fi

# Ensure staging owner repo is set when claiming ownership
if [[ "$NEW_STAGING_OWNER" == "true" && -z "$NEW_STAGING_OWNER_REPO" ]]; then
  NEW_STAGING_OWNER_REPO="$PROJECT_ROOT"
fi

# Validate base port
if ! [[ "$NEW_BASE_PORT" =~ ^[0-9]+$ ]]; then
  die "Invalid port number: $NEW_BASE_PORT"
fi

if [[ "$NEW_BASE_PORT" -lt 1024 || "$NEW_BASE_PORT" -gt 65500 ]]; then
  die "Port must be between 1024 and 65500"
fi

# Write config
write_config "$NEW_INSTANCE" "$NEW_BASE_PORT" "$NEW_K8S_OWNER" "$NEW_STAGING_OWNER" "$NEW_STAGING_OWNER_REPO" "$NEW_STAGING_KUBECONFIG"

# Show resulting config
echo ""
source "$SCRIPT_DIR/_config.sh"
eve_show_config
