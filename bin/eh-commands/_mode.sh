#!/usr/bin/env bash
# Mode tracking utilities for Eve Horizon
# Tracks which mode (local, docker, none) is currently active

# Compute PROJECT_ROOT if not already set
if [[ -z "${PROJECT_ROOT:-}" ]]; then
  _MODE_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
  PROJECT_ROOT="$(cd "$_MODE_DIR/../.." && pwd)"
fi

# Mode file location (gitignored)
EVE_MODE_FILE="$PROJECT_ROOT/.eve-mode"
EVE_PID_FILE="$PROJECT_ROOT/.eve-local-pids"

# Get current mode from .eve-mode file
# Returns: local, docker, or none
get_current_mode() {
  if [[ -f "$EVE_MODE_FILE" ]]; then
    grep "^mode=" "$EVE_MODE_FILE" 2>/dev/null | cut -d= -f2
  else
    echo "none"
  fi
}

# Get mode start time
get_mode_started_at() {
  if [[ -f "$EVE_MODE_FILE" ]]; then
    grep "^started_at=" "$EVE_MODE_FILE" 2>/dev/null | cut -d= -f2
  else
    echo ""
  fi
}

# Set current mode
# Args: mode (local, docker, none)
set_current_mode() {
  local mode="$1"
  if [[ "$mode" == "none" ]]; then
    rm -f "$EVE_MODE_FILE"
  else
    cat > "$EVE_MODE_FILE" << EOF
# Auto-generated by ./bin/eh start
# Do not edit manually
mode=$mode
started_at=$(date -u +"%Y-%m-%dT%H:%M:%SZ")
EOF
  fi
}

# Check if a different mode is running
# Args: requested_mode
# Returns: 0 if switch needed, 1 if same mode or none
needs_mode_switch() {
  local requested="$1"
  local current=$(get_current_mode)

  if [[ "$current" == "none" ]]; then
    return 1  # No switch needed, nothing running
  elif [[ "$current" == "$requested" ]]; then
    return 1  # Same mode, no switch needed
  else
    return 0  # Different mode, switch needed
  fi
}

# Save PID for local mode service
# Args: service_name pid
save_pid() {
  local service="$1"
  local pid="$2"
  echo "$service=$pid" >> "$EVE_PID_FILE"
}

# Get all saved PIDs
get_saved_pids() {
  if [[ -f "$EVE_PID_FILE" ]]; then
    cat "$EVE_PID_FILE"
  fi
}

# Clear PID file
clear_pids() {
  rm -f "$EVE_PID_FILE"
}

# Check if a PID is still running
is_pid_running() {
  local pid="$1"
  kill -0 "$pid" 2>/dev/null
}
